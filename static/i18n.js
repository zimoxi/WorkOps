  // ─── 语言切换 ──────────────────────────────────────────
  function getLang() {
    try {
      var stored = localStorage.getItem(STORAGE_KEY);
      if (stored === "zh" || stored === "en") return stored;
    } catch (_) {}
    return DEFAULT_LANG;
  }

  function setLang(lang) {
    if (lang !== "zh" && lang !== "en") return;
    try {
      localStorage.setItem(STORAGE_KEY, lang);
    } catch (_) {}
    if (_onLangChange) _onLangChange(lang);
  }

  function onLangChange(callback) {
    _onLangChange = callback;
  }

  // ─── 翻译函数 ──────────────────────────────────────────
  function t(key) {
    var lang = getLang();
    var d = dict[lang] || dict[DEFAULT_LANG];
    var val = d[key];
    if (val !== undefined) return val;
    // 回退到默认语言
    if (lang !== DEFAULT_LANG) {
      val = dict[DEFAULT_LANG][key];
      if (val !== undefined) return val;
    }
    // 最终回退：返回 key 本身
    return key;
  }

  // 带参数替换的翻译：t("overview.stepOf", {n: 1, total: 11})
  function tf(key, params) {
    var text = t(key);
    if (params) {
      Object.keys(params).forEach(function (k) {
        text = text.replace(new RegExp("\\{" + k + "\\}", "g"), params[k]);
      });
    }
    return text;
  }

  // ─── 语言切换 UI ──────────────────────────────────────
  function renderLangSwitcher() {
    var lang = getLang();
    return (
      '<div class="lang-switcher">' +
      '<button class="lang-btn' +
      (lang === "zh" ? " active" : "") +
      '" onclick="WorkOps.setLang(\'zh\')">中文</button>' +
      '<button class="lang-btn' +
      (lang === "en" ? " active" : "") +
      '" onclick="WorkOps.setLang(\'en\')">EN</button>' +
      "</div>"
    );
  }

  // ─── 公开 API ──────────────────────────────────────────
  return {
    t: t,
    tf: tf,
    getLang: getLang,
    setLang: setLang,
    onLangChange: onLangChange,
    renderLangSwitcher: renderLangSwitcher,
  };
